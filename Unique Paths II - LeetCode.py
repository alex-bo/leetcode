from typing import List


class Solution:

    def uniquePathsWithObstacles(self, obstacleGrid: List[List[int]]) -> int:
        # if 0x0 - 0 paths
        if not obstacleGrid:
            return 0

        row_len = len(obstacleGrid[0])
        for row in obstacleGrid:
            if len(row) != row_len:
                raise ValueError('Grid is invalid!')

        # if first position is an obstacle
        if obstacleGrid[0][0] == 1:
            return 0

        dp = [
            [0 for c in r] for r in obstacleGrid
        ]

        for m, r in enumerate(obstacleGrid):
            for n, c in enumerate(r):
                if c == 1:
                    continue
                if m == 0 and n == 0:
                    dp[m][n] = 1
                else:
                    dp[m][n] = sum((
                        0 if m <= 0 else dp[m - 1][n],
                        0 if n <= 0 else dp[m][n - 1],
                    ))

        return dp[len(obstacleGrid) - 1][len(obstacleGrid[0]) - 1]


def test_one(grid: List[List[int]], expected: int):
    print(grid)
    actual = Solution().uniquePathsWithObstacles(grid)
    if expected == actual:
        print('OK')
    else:
        print('WRONG! Got {}, expected {}!'.format(actual, expected))


